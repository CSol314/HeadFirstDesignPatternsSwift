public class PopcornPopper {
    
    public init() { }
    
    public func on() {
        print("Popcorn Popper on")
    }
    
    public func pop() {
        print("Popcorn Popper popping popcorn!")
    }
    
    public func off() {
        print("Popcorn Popper off")
    }
}
